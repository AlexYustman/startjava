public class Variable {
	public static void main(String[] args) {
		byte computerQuantity = 1;
		System.out.println("Kolichestvo kompjyuterov: " + computerQuantity);
		
		short purchaseDate = 2004;
		System.out.println("Data pokupki pervogo PC: " + purchaseDate);
		
		int core = 4;
		System.out.println("Skolko yader u CPU: " + core);
		
		long ram = 4294967;
		System.out.println("Operativnaya pamyat' v Mhz: " + ram);
		
		float cpuFrequency = 1.123456f;
		System.out.println("Chastota CPU v Ghz: " + cpuFrequency);
		
		double pi = 3.141592653589793238462643;
		System.out.println("Chislo Pi: " + pi);
		
		char symbol = 'X';
		System.out.println("Prosto simvol: " + symbol);
		
		boolean yes = true;
		System.out.println("I learn Java and it`s " + yes);
	}
}